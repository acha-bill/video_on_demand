package vod.models;

import org.springframework.data.annotation.Id;

public class Message {
    @Id
    private int id;
    private String message;

    public Message(int id, String message) {
        this.id = id;
        this.message = message;
    }

    public int getId() {
        return id;
    }

    public String getMessage() {
        return message;
    }
}
